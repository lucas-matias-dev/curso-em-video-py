var = input ('digite algo. ')

print (type(var))
print ('contem letras ou numeros?',var.isalnum())
print ('contem letras?',var.isalpha())
print ('é um valor decimal?',var.isdecimal())
print ('é um valor digito?',var.isdigit())
print ('é um valor imprimivel?',var.isprintable())
print ('é um valor identificador',var.isidentifier())
print ('contem valores no minusculo?',var.islower())
print ('contem valores no maiusculos?',var.isupper())
print ('contem espaço',var.isspace())
print ('é um valor todo em maiusculo?',var.istitle())
print ('é um valor numerico?',var.isnumeric())
